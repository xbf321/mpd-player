import type { Metadata } from 'next';
import './globals.css';

import { Provider } from './provider';

import { getStatus } from './api/status/route';

export const metadata: Metadata = {
  title: 'MPD Player by Nextjs',
  description: 'Generated by Nextjs',
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const status = (await getStatus()) as SongInfo | null;
  return (
    <html lang="en">
      <body className="p-4 flex flex-col h-screen max-w-lg m-auto">
        <Provider value={status}>{children}</Provider>
      </body>
    </html>
  );
}
